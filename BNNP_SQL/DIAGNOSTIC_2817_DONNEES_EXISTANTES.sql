WITH trans AS (
    SELECT
        OPERATIONNETAMOUNT,
        PAYMENTREFERENCE,
        NUMEROCLIENT,
        RIBBANKCODE,
        RIBBRANCHCODE,
        IDENTIFICATIONRIB,
        RIBCHECKDIGIT,
        SETTLEMENTMODE,
        TYPEREGLEMENT,
        IDENTIFICATION AS CODE_SOCIETE,
        OPERATIONNETAMOUNTCURRENCY
    FROM TA_RN_IMPORT_GESTION
    WHERE OPERATIONNETAMOUNT IN ('22.36', '2817')
)
SELECT
    T.OPERATIONNETAMOUNT AS MONTANT,
    T.RIBBANKCODE||T.RIBBRANCHCODE||T.IDENTIFICATIONRIB||T.RIBCHECKDIGIT AS RIB_COMPLET,
    CBS.ID_COMPTE_BANCAIRE_SYSTEME,
    GA.ID_COMPTE_ACCURATE,
    CA.NUM_COMPTE_ACCURATE,
    CA.TYPE_RAPPRO,
    T.SETTLEMENTMODE,
    T.TYPEREGLEMENT,
    T.CODE_SOCIETE,
    T.OPERATIONNETAMOUNTCURRENCY
FROM trans T
    LEFT JOIN TA_RN_COMPTE_BANCAIRE_SYSTEME CBS
        ON CBS.RIBBANKCODE = T.RIBBANKCODE
       AND CBS.RIBBRANCHCODE = T.RIBBRANCHCODE
       AND CBS.RIBIDENTIFICATION = T.IDENTIFICATIONRIB
       AND CBS.RIBCHECKDIGIT = T.RIBCHECKDIGIT
    LEFT JOIN TA_RN_GESTION_ACCURATE GA
        ON GA.ID_COMPTE_BANCAIRE_SYSTEME = CBS.ID_COMPTE_BANCAIRE_SYSTEME
    LEFT JOIN TA_RN_COMPTE_ACCURATE CA
        ON CA.ID_COMPTE_ACCURATE = GA.ID_COMPTE_ACCURATE
ORDER BY T.OPERATIONNETAMOUNT;

