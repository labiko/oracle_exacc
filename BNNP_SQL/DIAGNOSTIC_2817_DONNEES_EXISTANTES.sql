PROMPT 8. MODE CUMUL - Transaction 2817
PROMPT ============================================================================

WITH trans_2817 AS (
    SELECT * FROM TA_RN_IMPORT_GESTION WHERE OPERATIONNETAMOUNT = '2817' AND ROWNUM = 1
),
cbs_342 AS (
    SELECT CBS.ID_COMPTE_BANCAIRE_SYSTEME
    FROM TA_RN_GESTION_ACCURATE GA
        JOIN TA_RN_COMPTE_BANCAIRE_SYSTEME CBS ON CBS.ID_COMPTE_BANCAIRE_SYSTEME = GA.ID_COMPTE_BANCAIRE_SYSTEME
    WHERE GA.ID_COMPTE_ACCURATE = 342 AND ROWNUM = 1
)
SELECT
    T2817.NUMEROCLIENT,
    T2817.SETTLEMENTMODE,
    CBS.ID_COMPTE_BANCAIRE_SYSTEME,
    CASE WHEN CMR.ID_MODE_REGLEMENT IS NOT NULL THEN 'OUI ❌ MODE CUMUL ACTIF' ELSE 'NON ✅' END AS MODE_CUMUL
FROM trans_2817 T2817
    CROSS JOIN cbs_342 CBS
    LEFT JOIN TA_RN_MODE_REGLEMENT MR ON MR.CODE_MODE_REGLEMENT = T2817.SETTLEMENTMODE
    LEFT JOIN TA_RN_PRODUIT P ON P.CODE_PRODUIT = T2817.NUMEROCLIENT OR P.CODE_PRODUIT = 'ALL'
    LEFT JOIN TA_RN_CUMUL_MR CMR ON CMR.ID_MODE_REGLEMENT = MR.ID_MODE_REGLEMENT
        AND CMR.ID_PRODUIT = P.ID_PRODUIT
        AND CMR.ID_COMPTE_BANCAIRE_SYSTEME = CBS.ID_COMPTE_BANCAIRE_SYSTEME;

