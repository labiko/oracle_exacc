    CROSS JOIN cbs_342 CBS
    LEFT JOIN TA_RN_SOCIETE S ON S.CODE = T2817.IDENTIFICATION
    LEFT JOIN TA_RN_EXCLUSION_SOCIETE ES ON ES.ID_SOCIETE = S.ID_SOCIETE
        AND ES.ID_COMPTE_BANCAIRE_SYSTEME = CBS.ID_COMPTE_BANCAIRE_SYSTEME

