FROM trans_2817 T2817
    CROSS JOIN cbs_342 CBS
    LEFT JOIN TA_RN_DEVISE D ON D.CODE_ISO_DEVISE = T2817.OPERATIONNETAMOUNTCURRENCY
    LEFT JOIN TA_RN_EXCLUSION_DEVISE ED ON ED.ID_DEVISE = D.ID_DEVISE
        AND ED.ID_COMPTE_BANCAIRE_SYSTEME = CBS.ID_COMPTE_BANCAIRE_SYSTEME

UNION ALL

SELECT
    'Exclusion MODE_REGLEMENT' AS TYPE_EXCLUSION,
    T2817.SETTLEMENTMODE AS VALEUR,
    CASE WHEN EMR.ID_MODE_REGLEMENT IS NOT NULL THEN 'OUI ❌ EXCLUE' ELSE 'NON ✅' END AS EXCLUE
FROM trans_2817 T2817
    CROSS JOIN cbs_342 CBS
    LEFT JOIN TA_RN_MODE_REGLEMENT MR ON MR.CODE_MODE_REGLEMENT = T2817.SETTLEMENTMODE
    LEFT JOIN TA_RN_EXCLUSION_MR EMR ON EMR.ID_MODE_REGLEMENT = MR.ID_MODE_REGLEMENT
        AND EMR.ID_COMPTE_BANCAIRE_SYSTEME = CBS.ID_COMPTE_BANCAIRE_SYSTEME

